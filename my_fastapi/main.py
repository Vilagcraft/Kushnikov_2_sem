# Указываем маршрут /api/count, по которому можно получить результаты вычилений для Практической работы 1
# Тело данного POST-запрос имеет вид {"x0": <Словарь начальный значений>, "c": <Словарь констант>, "f": <Словарь полиномов>}
# Записи <Словарь начальный значений> имеют вид "Имя значения": <значение>
# Записи <Словарь констант> имеют вид "Имя константы": <значение>
# Записи <Словарь полиномов> имеют вид "Имя полинома": {"a": <значение>, "b": <значение>, "c": <значение>, "d": <значение> }
@app.post("/api/count")
async def count(data  = Body()):
    try:
        # Извлекаем пришедшие начальные значения в t0
        t0 = data['x0']

        # Извлекаем пришедшие коэффициенты полиномов в uf
        uf = data['f']

        # На основе коэфициентов uf создадим удобные для работы экземпляры класса F, каждый из которых сопоставив с названием
        # Соберм пары "Имя полинома" : <Экземпляр полинома> в словарь f
        f = {}
        for key in uf:
            f[key] = F(
                uf[key]['a'],
                uf[key]['b'],
                uf[key]['c'],
                uf[key]['d']
            )

        # Передаем в качетсве аргументов в функцию для решения системы диффуров odeint
        # Параметры:
        # du_dt - ранее описанная функция, выражающая систему уравнений Практической Работы 1
        # list(t0.values()) - список начальных значений
        # t_span - массив временных точек от 0 до 1
        # args=(f,) - дополнительный аргументы в виде констант и полиномов
        # full_output=True - для определения, решена ли система или нет
        # Получаем на выходе:
        # usolution - Решение системы в виде массива, элементы которого являются массивами решений для указанной точке на временной прямой
        # d - лог вычислений

        # usolution,d = odeint(du_dt, list(t0.values()), t_span, args=(f,), full_output=True)
        usolution = solve_ivp(
            du_dt,
            (0, 1),
            list(t0.values()),
            args=(f,),
            dense_output=True,
            rtol=1e-6,
            atol=1e-9,
        )

        # Если решение не определено для заданных параметров - вернуть ошибку
        if (not usolution.success):
            print("Error")
            raise HTTPException(status_code=500)

        t = [ i * 0.05 for i in range(21)]
        y = [
            [float(value) for value in array]
            for array in usolution.sol(t)
        ]

        return JSONResponse(content={"t": t, "y": y})

    except Exception as err:
        print(f"Unexpected {err=}, {type(err)=}")
        raise HTTPException(status_code=500)
