# Добавление заисимости для выполнения вычислений, связанных с диффурами
from scipy. integrate import odeint, solve_ivp

# Инициализация приложения FastAPI
app = FastAPI()

# Вспомогательный класс F, выражающий полином третьей степени.
# Поля:
# a - коэффициент при x^3
# b - коэффициент при x^2
# c - коэффициент при x
# d - свободный коэффициент
# Методы:
# calc(x) - вычисляет значение полинома при заданном x
class F:
    def __init__(self, a, b, c, d):
        self.a = a
        self.b = b
        self.c = c
        self.d = d

    def __call__(self, x):
        return self.a * (x**3) + self.b * (x**2) + self.c * x + self.d

    def calc(self, x):
        return self.a* (x**3) + self.b * (x**2) + self.c * x + self.d

# Функция, выражающая систему уравнений Практической Работы 1
# Принимает:
# u - массив исследуемых параметров, представляющих собой функции xi(t), i=[1..31]
# t - массив временных точек от 0 до 1
# с - словарь констант вида {"Название": <Значение>, ...}
# f - словарь полиномов вида {"Название": <Экземпляр полинома>, ...}
# Возвращает:
# массив вычисленных выражений dxi/dt, i=[1..31]
def du_dt(t, u, f):

        # Извлекаем элементы, которые являются исследуемыми xi(t), из массива u
        [x1_t, x2_t, x3_t, x4_t, x5_t, x6_t, x7_t, x8_t] = u

        # Для каждого уравнения приводим запись формулы из задания.
        # Для формулы:
        # dx1/dt = F3(t) - f1(x2) * f2(x3) * f3(x4), p.s. F <=> Z
        # программная запись примет вид:
        dx1_dt = (
            f['Z3'](t)
            -
            f['f1'](x2_t) * f['f2'](x3_t) * f['f3'](x4_t)
        )

        #Далее по аналогии со всеми уравнениями системы...
        dx2_dt = (
            ( f['Z1'](t) + f['Z2'](t)  + f['Z4'](t) ) * f['f4'](x4_t) * f['f5'](x6_t) * f['f6'](x4_t)
            -
            ( f['Z3'](t) + f['Z5'](t) ) * f['f7'](x8_t)
        )

        dx3_dt = (
            ( f['Z4'](t) ) * f['f8'](x7_t)
            -
            ( f['Z3'](t) + f['Z5'](t) ) * f['f9'](x1_t)
        )

        dx4_dt = (
            ( f['Z2'](t) ) * f['f10'](x2_t) * f['f11'](x7_t)
            -
            ( f['Z3'](t) + f['Z5'](t) ) * f['f12'](x1_t)
        )

        dx5_dt = (
            f['f13'](x2_t)
            -
            f['Z5'](t)
        )

        dx6_dt = (
            ( f['Z2'](t) ) * f['f14'](x2_t)
            -
            f['Z5'](t)
        )

        dx7_dt = (
            ( f['Z2'](t) ) * f['f15'](x2_t) * f['f16'](x3_t) * f['f17'](x4_t)
            -
            ( f['Z3'](t) + f['Z5'](t) )
        )

        dx8_dt = (
            f['Z5'](t)
            -
            f['f18'](x2_t)
        )

        # Возвращаем массив вычисленных dxi/dt, i=[1..31]
        return [dx1_dt, dx2_dt, dx3_dt, dx4_dt, dx5_dt, dx6_dt, dx7_dt, dx8_dt]

